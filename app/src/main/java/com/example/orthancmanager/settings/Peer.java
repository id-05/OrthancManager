package com.example.orthancmanager.settings;

class Peer {

    String mURL;
    String mLogin;
    String mPassword;
    String mName;

    String getmURL() {
        return mURL;
    }

    void setmURL(String mURL) {
        this.mURL = mURL;
    }

    String getmLogin() {
        return mLogin;
    }

    void setmLogin(String mLogin) {
        this.mLogin = mLogin;
    }

    String getmPassword() {
        return mPassword;
    }

    void setmPassword(String mPassword) {
        this.mPassword = mPassword;
    }

    String getmName() {
        return mName;
    }

    void setmName(String mName) {
        this.mName = mName;
    }


}
